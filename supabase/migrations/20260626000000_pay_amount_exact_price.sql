-- ============================================================================
-- РЕШЕНИЕ Temur'а: ученик платит РОВНО цену урока (фикс. сумму), БЕЗ кода-наценки.
--
-- Раньше pay_amount = цена + случайный код (1..900 сум) для уникальности →
-- по уникальной сумме опознавался платёж. Temur решил: убрать наценку, платить
-- ровно цену. Переопределяем _unique_pay_amount: pay_amount = точная цена,
-- БЕЗ кода. verify_code = 0 (наценки нет).
--
-- ВАЖНО про уникальность и безопасность денег:
--   • Округление до ЦЕЛОГО сума сохраняем: pay_amount = round(price/100)*100.
--     Paynet/SMS оперируют целыми сумами, матч по (pay_amount/100) должен быть
--     целым числом. (Для кратного 100 витрина formatTiyin делает /100 без потерь.)
--   • Без кода две брони с одинаковой ценой дают ОДИНАКОВЫЙ pay_amount. Это
--     допустимо: ingest_payment_sms при >1 совпадении возвращает 'ambiguous' и
--     НЕ авто-подтверждает — платёж уходит на ручное подтверждение в админке
--     (/payment-confirmations). Деньги не начисляются «не той» брони.
--   • Поэтому УБИРАЕМ прежний цикл «подобрать уникальную сумму среди pending»:
--     при отсутствии кода он либо зациклился бы (две одинаковые цены), либо
--     искусственно добавил бы наценку — а Temur её запретил.
--
-- Файл с timestamp 20260626 > обоих конкурирующих 20260625000002_* → применяется
-- ПОСЛЕДНИМ и гарантированно переустанавливает функцию поверх любой версии.
-- Аддитивно, идемпотентно (create or replace). Старые pending не трогаем —
-- они с кодом, истекут / добьются вручную; новые пойдут уже по точной цене.
-- ============================================================================

create or replace function public._unique_pay_amount(p_base bigint)
returns table (pay_amount bigint, verify_code int)
language plpgsql security definer set search_path = public as $$
begin
  -- РОВНО цена урока, округлённая до целого сума (×100 тийин). Без кода-наценки.
  pay_amount  := (round(p_base / 100.0))::bigint * 100;
  verify_code := 0;
  return next;
end $$;

revoke execute on function public._unique_pay_amount(bigint) from public, anon;
